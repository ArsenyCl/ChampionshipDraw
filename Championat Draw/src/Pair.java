public class Pair {
    private int dig;
    private String str;
    public Pair(int dig, String str) {
        this.dig = dig;
        this.str = str;
    }
    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public int getDig() {
        return dig;
    }

    public void setDig(int dig) {
        this.dig = dig;
    }
}